bool TMath::fCheckIp(const QString &Ip)
{
    QStringList StringList = Ip.split(tr("."));

    if(4 != StringList.length())
    {
        return false;
    }

    foreach(QString Data, StringList)
    {
        int IntData = Data.toInt();

        if(IntData < 0 || IntData > 255)
        {
            return false;
        }
    }

    return true;
}

