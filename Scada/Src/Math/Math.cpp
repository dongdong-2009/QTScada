int TMath::fGetBits(int _Byte, int _Offset, int _Len)
{
    if(_Offset > 7)
    {
        return 0;
    }

    _Byte &= 0xFF;

    if (_Offset + _Len > 8)
    {
        _Len = 8 - _Offset;
    }

    return (_Byte >> (8 - _Offset - _Len)) & (~(0xFF << _Len));
}

void TMath::fSetBytes(char *_pcDst, int _iStart, int _iBytes, int _iValue)
{
    for(int i = 0; i < _iBytes; i++)
    {
        _pcDst[_iStart + i] = (char)((_iValue >> (i * 8)) & 0xFF);
    }
}

int TMath::fGetBytes(const unsigned char *_pcSrc, int _Offset, int _Len)
{
    if (_Len < 1 || _Len > 4)
    {
        return 0;
    }

    int Result = 0;

    for(int i = 0; i < _Len; i++)
    {
        Result |= (_pcSrc[_Offset + i] << (8 * i));
    }

    return Result;
}

int TMath::fGetBytesHighFront(const unsigned char *_pcSrc, int _Offset, int _Len)
{
    if (_Len < 1 || _Len > 4)
    {
        return 0;
    }

    int Result = 0;

    for(int i = 0; i < _Len; i++)
    {
        Result |= (_pcSrc[_Offset + i] << (8 * (_Len - i - 1)));
    }

    return Result;
}

int TMath::fCheckSum(const void *pcSrc, int iLen)
{
    if(0 == pcSrc || iLen < 1)
    {
        return -1;
    }

    int i = 0;
    int cs = 0;

    for(i = 0; i < iLen; i++)
    {
        cs += *(((char *)pcSrc) + i);
    }

    return (cs % 256);
}

