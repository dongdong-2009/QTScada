quint16 TMath::s_Crc16Bit(quint8 *p_uch_Data, quint16 uin_CrcDataLen)
{
    quint8  uch_CRCHi = 0xFF ;
    quint8  uch_CRCLo = 0xFF ;
    quint16 uin_Index=0;

    while (uin_CrcDataLen--)
    {
       uin_Index = uch_CRCHi ^ *p_uch_Data++ ;
       uch_CRCHi = uch_CRCLo ^ ar_uch_CRCHi[uin_Index];
       uch_CRCLo = ar_uch_CRCLo[uin_Index] ;
    }

    return (((quint16)uch_CRCHi << 8) | ((quint16)(uch_CRCLo))) ;
}


